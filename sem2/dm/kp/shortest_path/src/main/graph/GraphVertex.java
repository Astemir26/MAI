package main.graph;

public class GraphVertex {
	public GraphVertex(double x, double y) {
		this.x = x;
		this.y = y;
	}

	public void setX(double x) {
		this.x = x;
	}

	public void setY(double y) {
		this.y = y;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	private double x;
	private double y;

	public static final double RADIUS = 24.0;
}
